// Get localized strings based on language
String getString(String key, AppLanguage language) {
  if (language == AppLanguage.turkish) {
    return _stringsMapTR[key] ?? key;
  } else if (language == AppLanguage.russian) {
    return _stringsMapRU[key] ?? key;
  } else {
    return _stringsMapEN[key] ?? key;
  }
}

